    private void showActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showActionPerformed
        // TODO add your handling code here:
        // 'show' adlı JCheckBox seçiliyse (işaretliyse) çalışacak blok
        if (show.isSelected()) {
            // JTextField'in echo karakterini sıfıra (0) ayarla, bu da şifreyi gösterir
            tfpassword.setEchoChar((char) 0); // Şifreyi göster
        } else {
            // 'show' seçili değilse (işaretli değilse) çalışacak blok
            // JTextField'in echo karakterini '*' karakterine ayarla, bu da şifreyi gizler
            tfpassword.setEchoChar('*'); // Şifreyi gizle
        }


    }//GEN-LAST:event_showActionPerformed


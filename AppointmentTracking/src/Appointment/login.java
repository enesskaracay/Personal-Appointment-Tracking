    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed

        // Login butonuna tıklandığında bu metodun çalıştığı kontrol ediliyor
        if (evt.getSource() == login) {
            // Kullanıcı adı ve şifre alanlarındaki metinleri al
            String username = tfsuername.getText();
            String password = tfpassword.getText();

            // Kullanıcı adı veya şifre boşsa hata mesajı göster ve metottan çık
            if (username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Lütfen boşlukları doldurunuz.");
                return;
            }

            // Veritabanında kullanıcı adı ve şifreyi kontrol etmek için SQL sorgusu oluştur
            // BINARY kullanarak büyük/küçük harf duyarlılığını sağla
            String query = "SELECT * FROM register WHERE BINARY username='" + username + "' AND BINARY password='" + password + "'";

            try {
                // Veritabanı bağlantısını kur
                Conn c = new Conn();

                // SQL sorgusunu gerçekleştir ve sonuçları al
                ResultSet result = c.s.executeQuery(query);

                // Eğer sonuçlar varsa, kullanıcı doğrulandı demektir
                if (result.next()) {
                    // Giriş ekranını kapat
                    setVisible(false);

                    // Yeni bir Best JFrame'i aç ve görünür yap
                    new Best(username).setVisible(true);

                    // Şuanki JFrame'i kapat
                    dispose();
                } else {
                    // Kullanıcı doğrulanamadı, hata mesajı göster
                    JOptionPane.showMessageDialog(null, "Kullanıcı adı veya şifre hatalı.");
                }

            } catch (Exception e) {
                // Hata durumunda hata izlemeyi etkinleştir ve hatayı yazdır
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_loginActionPerformed

